numbers = [1, 5, 2, 8, 3]
print(f'orignal numbers: {numbers}')


# Sorting the list
numbers.sort()
print(f"Sorted numbers: {numbers}")

# Reversing the list
numbers.reverse()
print(f"Reversed numbers: {numbers}")

# Getting the length of the list
print(f"Length of numbers list: {len(numbers)}")

# Counting occurrences of an element
fruits = ["apple", "banana", "apple"]
print(f'orignal fruits: {fruits}')
print(f"Count of 'apple': {fruits.count('apple')}")